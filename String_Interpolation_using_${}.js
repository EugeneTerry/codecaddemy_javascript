// here we use the `${}` to insert a variable into a sentence to display as its value.
// you have to use the " ` " symbol next to the "1" on the keyboard.
let myName = 'Eugene'
let myCity = 'Antioch'
console.log(`My name is ${myName}. My favorite city is ${myCity}.`) // this displays "My name is Eugene. My favorite city is Antioch."
let studentFirst = 'Gene'
let studentLast = 'Terry'
console.log(`My first name is ${studentFirst} and my last name is ${studentLast}.`);
